package Ch3FunctionsandMethods;

public class FunctionVariableandScope {

	public static void main(String[] args) {
		
	    int number = 10;
	    // declare flag in the main()
	    // method scope
	     boolean flag = false;;

	     if (number > 0) {
	       flag = true;
	     }

	     System.out.println(flag);
	   }	
};
	


